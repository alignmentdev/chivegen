/***
  An enum for verbosity settings.
  SILENT = no output.
  BRIEF = minimal output, errors.
  NORMAL = default level of print output.
  VERBOSE = includes additional warnings, status messages, etc.
  DEBUG = excessive output on inner workings, status etc. Use with caution.
***/
public enum Verbosity {
  SILENT, BRIEF, NORMAL, VERBOSE, DEBUG
}
