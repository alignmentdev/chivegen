/***
  An enum for ratings.
  Intended as a compromise between FFN and AO3's approach: 
    UNRATED = No rating given
    G = AO3 G, FFN K
    PG = AO3 G/T, FFN K+
    TEEN = AO3 T, FFN T
    MATURE = AO3 M, FFN MA
    EXPLICIT = AO3 E
***/
public enum Rating {
  UNRATED, G, PG, TEEN, MATURE, EXPLICIT
}
